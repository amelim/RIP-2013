short int Clause::a2ClauseCount[MAX_VARIABLES*2];

void Clause::vOutput()
{
  for (int i=0; i<_iVariableCount; i++) {
    if (bIsNegated(i))
      cout << '-';
    cout << eConstrainedVariable(i)+1;
    cout << ' ';
  }
  cout << '\n';
}
void Clause::vOutput(ofstream &Out)
{
  for (int i=0; i<_iVariableCount; i++) {
    if (bIsNegated(i))
      Out << '-';
    Out << eConstrainedVariable(i)+1;
    Out << ' ';
  }
  Out << '\n';
}

